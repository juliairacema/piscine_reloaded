void	ft_putchar(char c);

int		ft_count_if(char **tab, int(*f)(char*))
{
	int		count;

	count = 0;
	while (*tab != '\0')
	{
		if (f(*tab) == 1)
			count++;
		tab++;
	}
	return (count);
}
