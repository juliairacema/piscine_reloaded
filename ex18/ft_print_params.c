void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	while (*str)
	{
		ft_putchar(*str);
		str++;
	}
}

int		main(int argc, char **argv)
{
	int i;

	i = 0;
	
	while (--argc)
	{
		ft_putstr(argv[++i]);
		ft_putchar('\n');
	}
	return (0);
}
